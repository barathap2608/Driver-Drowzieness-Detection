def classify_fatigue(pred_index):
    label = inv_map[pred_index]
    if label in ["Open", "no_yawn"]:
        return 0, "Alert"
    elif label == "yawn":
        return 1, "Mild Fatigue"
    elif label == "Closed":
        return 2, "Severe Fatigue"

fatigue_results = [classify_fatigue(p) for p in y_pred_classes]

for pred, (stage, label) in zip(y_pred_classes[:10], fatigue_results[:10]):
    print(f"Predicted class: {inv_map[pred]:<10} → Fatigue Stage: {label}")

fatigue_stages = [stage for stage, label in fatigue_results]

